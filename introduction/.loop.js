//---while loop----

let counter = 0;  //initialization
while (counter <10){  //condition check
  console.log(`${counter+1}`)
  counter = counter +1;  //update
}
console.log ("out of the loop");

// ------for loop-----
/**
 for (initailiation , condition, update){
     //body of the loop
 }
 */
for(let c = 0 ; c<12 ; c++){
  console.log(`${c+1}`);
}